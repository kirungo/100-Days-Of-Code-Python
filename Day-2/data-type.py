#Using type ot get data type
print(type("Hello"[0])) #will print the first element
print(type("Hello"[-1])) #will print the last element

#string
print(type("123" + "456"))

#Interger = Whole number
print(type(123 + 435))

#Large Intergers
print(type(123_456_678))

#Float
print(type(3.14159))

#Boolean
print(type(True))
print(type(False))
