# Here is what a list looks like in Python
fruits = ["Cherry", "Apple", "Mango", "Pear"]

# Prints the whole list
print(fruits)

# [0] prints the first item on the list
print(fruits[0])

# [-1] prints the last item on the list
print(fruits[-1])

# Supposing i want to change the fruit at position 2 in my list this is how i will do it
fruits[1] = "Banana"
print(fruits[1])