# For Loops in Python

# This is a list of fruits
fruits = ["Apple", "Peach", "Pear", "Mango"]

# for loop - fruit is the variable that lets us loop through our list
# "fruit" is just a temporary variable name you choose
# It holds one item from the list at a time as Python loops through
# You could call it anything: item, x, f, etc.
# The pattern is: for [variable_name] in [list_name]:
for fruit in fruits:
    print(fruit)