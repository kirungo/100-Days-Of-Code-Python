# Functions WITHOUT inputs, arguments and parameters
def greet():
    print("Hello Terry!")
    print("How was your day?")
    print("Tomorrow, you are up early how do you feel about that?\n")

greet()

# Functions WITH inputs, arguments and parameters
def greeting(person_name):
    print(f"Hello {person_name}!")
    print("How are you today? ")
    print("I have a food delivery for you! ")

greeting("Mamu")